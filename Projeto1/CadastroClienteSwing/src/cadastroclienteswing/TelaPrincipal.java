    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        String tel = txtTel.getText();
        String End = txtEnd.getText();
        String num = txtNumero.getText();
        String cep = txtCep.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
       
        if(!isCamposValidos(nome,cpf,tel,num,End ,cep,cidade,estado)){
            JOptionPane.showMessageDialog(null, "Existem campos a serem preenchidos", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
        }
        
       
        Cliente cliente = new Cliente(nome, cpf,tel,End,num, cep, cidade, estado);
        Boolean isCadastrado = this.clienteDAO.cadastrar(cliente);
        
        if(isCadastrado){
            modelo.addRow(new Object[]{cliente.getNome(),cliente.getCpf(),cliente.getTel(),cliente.getEnd()});
            limparCampos();
            JOptionPane.showMessageDialog(null,"Cadastro concluído com sucesso!","OPERAÇÃO CONCLUIDA",JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null,"Cliente já se encontra cadastrado no sistema","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
                                             
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tabelaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClientesMouseClicked
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        String cpf = (String) tabelaClientes.getValueAt(linhaSelecionada, 1);
        
        Cliente cliente = this.clienteDAO.consultar(cpf);
        
        txtNome.setText(cliente.getNome());
        txtCPF.setText(cliente.getCpf());
        txtTel.setText(cliente.getTel().toString());
        txtEnd.setText(cliente.getEnd());
        txtNumero.setText(cliente.getNumero().toString());
        txtCep.setText(cliente.getCep());
        txtCidade.setText(cliente.getCidade());
        txtEstado.setText(cliente.getEstado());
    }//GEN-LAST:event_tabelaClientesMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        
        if(linhaSelecionada >= 0 ){
            int result = JOptionPane.showConfirmDialog(this, "Deseja realmente exluir esse cliente?", "CUIDADO!",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            
            if(result == JOptionPane.YES_OPTION){
                String cpf = (String) tabelaClientes.getValueAt(linhaSelecionada, 1);
                this.clienteDAO.excluir(cpf);
                modelo.removeRow(linhaSelecionada);
                
                JOptionPane.showMessageDialog(null, "Cliente excluído sucesso", "SUCESSO",JOptionPane.INFORMATION_MESSAGE);
                limparCampos();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Nenhum Cliente selecionado", "ERRO",JOptionPane.INFORMATION_MESSAGE);
        }
                
        
    }//GEN-LAST:event_btnExcluirActionPerformed

