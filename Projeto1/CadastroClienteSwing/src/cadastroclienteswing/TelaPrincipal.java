    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        
        if (linhaSelecionada < 0) {
        JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado!", 
            "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

        String cpf = (String) tabelaClientes.getValueAt(linhaSelecionada, 1);
        
        String nome   = txtNome.getText();
        String tel    = txtTel.getText();
        String end    = txtEnd.getText();
        String num    = txtNumero.getText();
        String cep    = txtCep.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();

        if (!isCamposValidos(nome, cpf, tel, num, end, cep, cidade, estado)) {
            JOptionPane.showMessageDialog(null, "Existem campos a serem preenchidos", 
                "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int result = JOptionPane.showConfirmDialog(this, 
            "Deseja alterar as informações selecionadas?", "ATENÇÃO!",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (result == JOptionPane.YES_OPTION) {
            // ✅ Atualiza todos os campos
            Cliente clienteAtualizado = new Cliente(nome, cpf, tel, end, num, cep, cidade, estado);
            this.clienteDAO.alterar(clienteAtualizado);
            atualizarTabela(clienteAtualizado, linhaSelecionada);
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso!", 
                "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Operação cancelada", 
                "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
        }
            
    }//GEN-LAST:event_btnAtualizarActionPerformed

